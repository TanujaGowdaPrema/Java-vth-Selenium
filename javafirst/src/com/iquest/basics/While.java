package com.iquest.basics;

public class While {
	

	public static void main(String[] args) {
		
		//Syntax
	
		/*
		 * initialization 
		 * while(condition) { 
		 * 		Statements; 
		 * 		increment/decrement }
		 */		
		
		/*
		 * int i = 0; 
		 * while (i<=5) { 
		 * System.out.println("hey"); 
		 * i++; }
		 */
		
		// DO while
		
		int j = 0;
		
		do {
			System.out.println("Hey ");
		}
		while (j<0);
		}
		
	}


